'use client';

import { SVGProps } from 'react';

// Icon size presets
const iconSizes = {
  xs: 'w-3 h-3',
  sm: 'w-4 h-4',
  md: 'w-5 h-5',
  lg: 'w-6 h-6',
  xl: 'w-8 h-8',
  '2xl': 'w-10 h-10',
} as const;

export type IconSize = keyof typeof iconSizes;

export interface IconProps extends Omit<SVGProps<SVGSVGElement>, 'size'> {
  name: IconName;
  size?: IconSize | number;
  className?: string;
}

/**
 * Icon Component
 *
 * A unified icon system using inline SVG sprites for optimal performance.
 * Supports consistent sizing, colors, and accessibility.
 *
 * @example
 * ```tsx
 * <Icon name="check" size="md" className="text-green-500" />
 * <Icon name="arrow-right" size={24} />
 * ```
 */
export default function Icon({
  name,
  size = 'md',
  className = '',
  ...props
}: IconProps) {
  const IconComponent = icons[name];

  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  const sizeClass =
    typeof size === 'string' ? iconSizes[size] : `w-[${size}px] h-[${size}px]`;

  return (
    <IconComponent
      className={`${sizeClass} ${className}`}
      aria-hidden="true"
      {...props}
    />
  );
}

